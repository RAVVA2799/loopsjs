//while loop program

var text = "";
var i = 0;
while (i < 10) {                     //while loop condition
  text += "<br>The number is "; + i;
  i++;                              //increment operator...
}
console.log(text);              //final outoput


//for loop 


var text = "";

for (var i = 0; i < 5; i++)            //for condition


{
  text += "The number is " + i + "<br>";            
}

console.log(text);                       //final output